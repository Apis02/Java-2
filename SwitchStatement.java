class SwitchStatement
{
public static void main (String[] args){
	

	int week = 4;
	String day;


	switch (week)

	{

	case 1:
	day =	"Sunday";
	break;

	case 2:
	day = "Monday";
	break;

	case 3:
	day= "Tuesday";
	break;

	case 4:
	System.out.println("Wednesday");
	break;

	default:
	System.out.println("No day");
	break;

	}

}
}